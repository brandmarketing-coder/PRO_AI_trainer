function buildQuizNext(module, askedQuestions) {
  const scope =
    module === "random"
      ? `從七大模組隨機抽題：${config.quizModules.map((m) => `${m.name}（${m.scope}）`).join("；")}`
      : (() => {
          const m = config.quizModules.find((x) => x.id === module);
          return `模組「${m.name}」：${m.scope}`;
        })();
  return `【目前功能：隨機測驗（逐一測驗）】
出題範圍：${scope}
依知識庫出「一題」測驗題。題型可為：情境題、知識題、話術題、判斷題（禁用話術辨識）。
只需要呼叫 search_knowledge 一次，挑一個具體主題（例如一項產品、一個常見異議、一條禁用話術）查詢即可出題，不要為了挑選題目而反覆查詢多個主題。
${askedQuestions.length ? `已考過的題目（不要重複或高度相似）：\n${askedQuestions.map((q, i) => `${i + 1}. ${q}`).join("\n")}` : ""}
輸出：module（模組名稱）、type（題型）、question（題目，情境題請把情境描述完整）、focus（評分重點，一句話）、reference（依剛剛查到的知識庫內容寫出的完整參考答案，之後批改時會直接使用、不會再查知識庫，所以要把關鍵事實寫清楚；不會顯示給作答者看）。題目要考業務實際會遇到的內容，答案必須能在知識庫中找到依據。`;
}

